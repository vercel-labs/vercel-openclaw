#!/usr/bin/env node
import { createHash } from "node:crypto";
import {
  copyFileSync,
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { join, resolve } from "node:path";

const repoRoot = resolve(process.cwd());
const sourceDir = resolve(repoRoot, "openclaw", "dist-vercel-runtime");
const targetDir = resolve(
  repoRoot,
  "src",
  "server",
  "openclaw",
  "runtime-artifact",
);
const generatedPath = resolve(
  repoRoot,
  "src",
  "server",
  "openclaw",
  "runtime-artifact.generated.ts",
);

function sha256File(filePath) {
  const hash = createHash("sha256");
  hash.update(readFileSync(filePath));
  return hash.digest("hex");
}

function fail(message) {
  console.error(message);
  process.exit(1);
}

const manifestPath = join(sourceDir, "manifest.json");
if (!existsSync(manifestPath)) {
  fail(
    [
      `Missing ${manifestPath}.`,
      "Run the fork build first:",
      "  cd openclaw",
      "  pnpm build:vercel-runtime-artifact",
    ].join("\n"),
  );
}

const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
const tarballPath = join(sourceDir, manifest.tarballFile);
if (!existsSync(tarballPath)) {
  fail(`Missing tarball referenced by manifest: ${tarballPath}`);
}

const actualSha256 = sha256File(tarballPath);
if (actualSha256 !== manifest.sha256) {
  fail(
    `SHA-256 mismatch for ${manifest.tarballFile}: expected ${manifest.sha256}, got ${actualSha256}`,
  );
}

mkdirSync(targetDir, { recursive: true });
for (const entry of readdirSync(targetDir)) {
  if (entry === "manifest.json" || entry.endsWith(".tgz")) {
    rmSync(join(targetDir, entry), { force: true });
  }
}

copyFileSync(manifestPath, join(targetDir, "manifest.json"));
copyFileSync(tarballPath, join(targetDir, manifest.tarballFile));

const generated = `/* auto-generated by scripts/vendor-openclaw-runtime-artifact.mjs */
/* eslint-disable */

export type VendoredOpenClawRuntimeArtifactManifest = {
  schemaVersion: 1;
  packageName: "openclaw";
  packageSpec: string;
  version: string;
  sha256: string;
  tarballFile: string;
  builtAt: string;
  gitCommit: string | null;
};

export const VENDORED_OPENCLAW_RUNTIME_ARTIFACT = {
  manifest: ${JSON.stringify(manifest, null, 2)} as const satisfies VendoredOpenClawRuntimeArtifactManifest,
  tarballUrl: new URL("./runtime-artifact/${manifest.tarballFile}", import.meta.url),
} as const;
`;

writeFileSync(generatedPath, generated);

console.log(
  JSON.stringify({
    event: "openclaw.vendor_runtime_artifact.complete",
    packageSpec: manifest.packageSpec,
    version: manifest.version,
    tarballFile: manifest.tarballFile,
    sha256: manifest.sha256,
    targetDir,
    generatedPath,
  }),
);
